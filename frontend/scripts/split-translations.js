#!/usr/bin/env node
// One-time script to split translations.ts into translations.en.ts and translations.fr.ts.
// Run: node scripts/split-translations.js
const fs = require('fs');
const path = require('path');

const src = path.resolve(__dirname, '../src/app/core/i18n/translations.ts');
const enOut = path.resolve(__dirname, '../src/app/core/i18n/translations.en.ts');
const frOut = path.resolve(__dirname, '../src/app/core/i18n/translations.fr.ts');

const text = fs.readFileSync(src, 'utf8');
const entries = [];

// Scan for all top-level entries of the form `'key': { en: '...', fr: '...' }`.
// We walk the string tracking braces and string literals.
let i = 0;
while (i < text.length) {
  // Find a top-level quoted key followed by colon and opening brace.
  // The key uses single quotes.
  const rest = text.slice(i);
  const keyStart = rest.indexOf("'");
  if (keyStart === -1) break;
  const keyEnd = rest.indexOf("'", keyStart + 1);
  if (keyEnd === -1) break;
  const key = rest.slice(keyStart + 1, keyEnd);
  // After the key, expect optional whitespace, colon, optional whitespace, opening brace.
  let j = keyEnd + 1;
  while (j < rest.length && /\s/.test(rest[j])) j++;
  if (rest[j] !== ':') {
    i += keyStart + 1;
    continue;
  }
  j++;
  while (j < rest.length && /\s/.test(rest[j])) j++;
  if (rest[j] !== '{') {
    i += keyStart + 1;
    continue;
  }
  j++; // skip the '{'
  // Now find the matching '}'. Track nested braces and string literals.
  const objStartInText = i + j;
  let depth = 1;
  let k = j;
  while (k < rest.length && depth > 0) {
    const ch = rest[k];
    if (ch === '{') depth++;
    else if (ch === '}') {
      depth--;
      if (depth === 0) break;
    } else if (ch === "'" || ch === '"') {
      const quote = ch;
      k++;
      while (k < rest.length) {
        if (rest[k] === '\\') {
          k += 2;
          continue;
        }
        if (rest[k] === quote) {
          k++;
          break;
        }
        k++;
      }
      continue;
    }
    k++;
  }
  const objText = rest.slice(j, k);
  function extract(field) {
    const re = new RegExp(field + "\\s*:\\s*(['\"`])([\\s\\S]*?)\\1");
    const m = objText.match(re);
    if (!m) return null;
    return m[2]
      .replace(/\\'/g, "'")
      .replace(/\\"/g, '"')
      .replace(/\\n/g, '\n')
      .replace(/\\t/g, '\t')
      .replace(/\\\\/g, '\\');
  }
  const en = extract('en');
  const fr = extract('fr');
  if (en != null && fr != null) {
    entries.push({ key, en, fr });
  }
  i = i + k + 1;
}

const enFile = `// English translations - eagerly loaded (in main bundle).
// Generated by scripts/split-translations.js. Do not edit by hand.
export const TRANSLATIONS_EN: Record<string, string> = {
${entries.map((e) => `  '${e.key}': ${JSON.stringify(e.en)}`).join(',\n')},
};
`;

const frFile = `// French translations - lazy-loaded on language switch.
// Generated by scripts/split-translations.js. Do not edit by hand.
export const TRANSLATIONS_FR: Record<string, string> = {
${entries.map((e) => `  '${e.key}': ${JSON.stringify(e.fr)}`).join(',\n')},
};
`;

fs.writeFileSync(enOut, enFile);
fs.writeFileSync(frOut, frFile);
console.log(`Split ${entries.length} keys into translations.en.ts and translations.fr.ts`);
